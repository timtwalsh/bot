def check_card_ranges():
    """
    searches every template card and compares each range, ensuring the first value is smaller than the second
    """
    for rarity in CARD_DATA_TEMPLATES:
        for card_name in CARD_DATA_TEMPLATES[rarity]:
            card = CARD_DATA_TEMPLATES[rarity][card_name]
            check_ranges = ["health_range",
                        "attack_range", 
                        "armor_range",
                        "barrier_range",
                        "value_range"]
            print(f'{card_name}', end=", ")
            for range_key in check_ranges:
                if range_key in card:
                    range_tuple = card[range_key]
                    assert range_tuple[0] <= range_tuple[1], f"{rarity} {card_name} {range_key} {range_tuple[0]} {range_tuple[1]}"


def check_card_name_lengths(max_length):
    """
    searches every template card and ensures the name is not longer than max_length characters and not more than max_words words
    """
    for rarity in CARD_DATA_TEMPLATES:
        print(f'...{rarity}')
        for card_name in CARD_DATA_TEMPLATES[rarity]:
            print(f'Checking {card_name} {len(card_name)}')
            assert len(card_name) <= max_length, f"{card_name} is longer than {max_length} characters"

